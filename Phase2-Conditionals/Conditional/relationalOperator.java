package Conditional;

public class relationalOperator {

     public static void main(String[] args) {
        int a=8;
        int b=12;

        System.out.println(a==b);//f
        System.out.println(a!=b);//t
        System.out.println(a>b);//f
        System.out.println(a<b);//t
        System.out.println(a>=b);
        System.out.println(a<=b);
     }

    
}
