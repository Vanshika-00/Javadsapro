public class intByInt {
    public static void main(String[] args) {
        double x=5;
        double y=2;
        System.out.println(x/y);

        double z=5/2;
        System.out.println(z);

        System.out.println(5/2);
        System.out.println(5.0/2.0);
        System.out.println(5.0/2);
        System.out.println(5/2.0);

    }
    
}
